    public void Listar(){
        DefaultTableModel lista = (DefaultTableModel) jTRegistro.getModel();
        lista.setRowCount(0);
        for(VeicAdd veicadd : Dados.getListaVeic()){
            lista.addRow(new Object[]{
                veicadd.getMarca(),
                veicadd.getModelo(),
                veicadd.getPlaca(),    
                veicadd.getAnoFabricacao(),
                veicadd.getQuilometragem(),
                veicadd.getCombustivel(),
                String.format("R$%.2f", veicadd.getPreco()),
                String.format("R$%.2f", veicadd.getValorDeprec())
            });
        }    
    }
