    private void jBAtribuirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAtribuirActionPerformed
        if(jTFMarca.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Marca obrigatório!", "Aviso", JOptionPane.ERROR_MESSAGE);
            jTFMarca.requestFocus();
            return;
        }
        if(jTFModelo.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Modelo obrigatório!", "Aviso", JOptionPane.ERROR_MESSAGE);
            jTFModelo.requestFocus();
            return;
        }
        if((jFTFPlaca.getText().trim().isEmpty()) || (jFTFPlaca.getText().length() != 8)){
            JOptionPane.showMessageDialog(null, "Verifique se a placa informada está correta!", "Aviso", JOptionPane.ERROR_MESSAGE);
            jFTFPlaca.requestFocus();
            return;
        }      
                 
        int Ano, Quilo = 0;
        double valor = 0;
        try {
            Ano = Integer.parseInt(jTFAnoFabric.getText());
            Quilo = Integer.parseInt(jTFQuilometra.getText());
            valor = Double.parseDouble(jTFPreco.getText());
            if ((Ano <= 1910) || (Ano >= 2022)){
                JOptionPane.showMessageDialog(null, "Ano de fabricação inválido!", "Aviso", JOptionPane.ERROR_MESSAGE);
                jTFAnoFabric.requestFocus();   
                return;
            }
            if(Quilo < 0){
                JOptionPane.showMessageDialog(null, "Quilometragem inválida(valor negativo)!", "Aviso", JOptionPane.ERROR_MESSAGE);
                jTFQuilometra.requestFocus();   
                return;
            }
            if(valor < 0){
                JOptionPane.showMessageDialog(null, "Preço inválido(valor negativo)!", "Aviso", JOptionPane.ERROR_MESSAGE);
                jTFPreco.requestFocus();     
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Erro numérico, verifique os campos Ano de fabricação, Quilometragem ou Preço!", "Aviso", JOptionPane.ERROR_MESSAGE);
            jTFAnoFabric.requestFocus();
            return;            
        }     
        
        VeicAdd veicadd = new VeicAdd(jTFMarca.getText(), jTFModelo.getText(), jFTFPlaca.getText(), Ano, Quilo, jTFCombust.getText(), valor);
        Dados.getListaVeic().add(veicadd);
        
        jTFMarca.setText("");
        jTFModelo.setText("");
        jFTFPlaca.setText("");
        jTFPreco.setText("");
        jTFQuilometra.setText("");
        jTFAnoFabric.setText("");
        jTFCombust.setText("Gasolina");       
        
        jTFMarca.requestFocus();
    }//GEN-LAST:event_jBAtribuirActionPerformed

